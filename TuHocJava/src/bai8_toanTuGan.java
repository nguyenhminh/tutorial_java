public class bai8_toanTuGan {

  public static void main(String[] args) {
    //Gán cộng
    int x = 8;

    x += 5; //x = x + 5
    System.out.println("x = " + x);

    //Gán trừ
    int x1 = 8;

    x1 -= 5; //x1 = x1 + 5
    System.out.println("x1 = " + x1);

    //Gán nhân
    int x2 = 8;

    x2 *= 5; //x2 = x2 * 5
    System.out.println("x2 = " + x2);

    //Gán chia
    float x3 = 8f;

    x3 /= 5; //x3 = x3 / 5
    System.out.println("x3 = " + x3);
  }
}
