public class bai4_kieuDuLieu {
    public static void main(String[] args) {
        //Kiểm tra kiểu nguyên
        System.out.println("max của kiểu nguyên int " + Integer.MAX_VALUE);
        System.out.println("min của kiểu nguyên int " + Integer.MIN_VALUE);

        //Kiểm tra kiểu byte
        System.out.println("max của kiểu nguyên byte " + Byte.MAX_VALUE);
        System.out.println("min của kiểu nguyên byte " + Byte.MIN_VALUE);
    }
}
